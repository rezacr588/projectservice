package com.pmd.project.model;

public class Budget {
  private double total;
  private double spent;

  // Getters and Setters

  public double getTotal() {
    return total;
  }

  public void setTotal(double total) {
    this.total = total;
  }

  public double getSpent() {
    return spent;
  }

  public void setSpent(double spent) {
    this.spent = spent;
  }
}